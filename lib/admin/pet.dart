class Pet {
  final String name;
  final String especie;
  final String idade;
  final String sexo;
  final String imageUrlAdc;
  final String raca;
  final String peso;
  final String bio;
  final String castrado;
  final bool hasDeficiencia;
  final String deficiencia;
  final bool hasMicrochip;
  final String microchip;

  Pet({
    required this.name,
    required this.especie,
    required this.idade,
    required this.sexo,
    required this.imageUrlAdc,
    required this.raca,
    required this.peso,
    required this.bio,
    required this.castrado,
    required this.hasDeficiencia,
    required this.deficiencia,
    required this.hasMicrochip,
    required this.microchip,
  });
}
